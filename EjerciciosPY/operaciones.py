#Sumar
n1 = int (input("Ingrese el primer número: "))
n2 = int (input("Ingrese el segundo número: "))
sum = n1 + n2
print("El resultado de la suma es: ",sum)

#Suma de numeros decimales
n1 = float(input("Ingrese el primer número decimal: "))
n2 = float(input("Ingrese el segundo número decimal: "))
sum = n1 + n2
print("El resultado de los números decimales son: ", sum)


